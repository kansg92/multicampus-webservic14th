package cho4;



public class P119 {

	public static void main(String[] args) {
		
		System.out.println("Start .....");
		
		for (int i = 1; i < 5; i++) {
			System.out.println("For ...." + i);
		} //end for
		
		System.out.println("End .....");
		
		int s = 1;
		while (s <= 10) {
			System.out.println("While ...." + s);
			s++;
		} //end while
		
	}

}
